function createWorld(){//инициализирует мир
	var n=size_world, m=size_world;
	for (var i=0; i<m; i++){
		world[i]=[];
		for (var j=0; j<n; j++){
			world[i][j]=0;
		}
		world[i][80-i]=-1;
		world[i][i]=-1;
	}
}

