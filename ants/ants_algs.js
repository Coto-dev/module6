function drawField(){
	ctx.clearRect(0, 0, 800, 800);
	for (var i=0; i<size_world; i++){
		for (var j=0; j<size_world; j++){
			if (world[i][j]==-1){
				ctx.fillStyle = 'black';
				ctx.fillRect(j*10, i*10, 10, 10);
			}
			else 
				if(world[i][j]==-2){
					ctx.fillStyle = 'red';
					ctx.fillRect(j*10, i*10, 20, 20);
				}
				else
				if(world[i][j]>0){
					ctx.fillStyle = 'green';
					ctx.fillRect(j*10, i*10, 20, 20);
				}
		}
	}
    drawAnt();
}


