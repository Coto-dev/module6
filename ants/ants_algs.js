function update_fer(){
	for(let i=0;i<feromon.length;i++){
		feromon[i].time --;
		}
	}
	for(let i=0;i<feromon.length;i++){
		if (feromon[i].time == 0){
			feromon[i].x+=0;feromon[i].y+=0;
			world[feromon[i].x][feromon[i].y]--;
			feromon.shift();
			i--;
	}
}

function new_fer(x1,y1){
	feromon.push({
		time: 10,
		x: x1,
		y: y1,
	})
	x1+=0;y1+=0;
	world[x1][y1]++;
}

